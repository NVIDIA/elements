/**
 * provides a object with key/value of the currently applied design tokens
 */
export function getThemeTokens(element = globalThis.document.querySelector(':root')) {
  // default root or provided element
  const styles = getComputedStyle(element);
  let parent = [];

  // check for parent iframe, same domain iframes change how the browser accesses style sheets
  try {
    parent = Array.from(globalThis.parent.document.styleSheets);
  } catch {
    // if not accessible, skip
  }

  return [...parent, ...Array.from(globalThis.document.styleSheets)]
    .reduce((finalArr, sheet) => {
      let rules = [];
      try {
        rules = [...sheet.cssRules];
      } catch {
        // if the style sheet is not accessible, skip
      }
      return finalArr.concat(
        rules
          .filter(rule => rule.type === 1)
          .reduce((propValArr, rule) => {
            const props = [...rule.style]
              .filter(p => p.trim().includes('--nve'))
              .map(propName => [propName.trim(), rule.style.getPropertyValue(propName).trim()]);
            return [...propValArr, ...props];
          }, [])
      );
    }, [])
    .reduce((p, token) => ({ ...p, [token[0]]: styles.getPropertyValue(token[0]) }), {});
}

export function removeEmptySlotWhitespace(slot: HTMLSlotElement) {
  if (slotContainsOnlyWhitespace(slot)) {
    slot.assignedNodes().forEach((node: HTMLElement) => node.remove());
  }
}

export function slotContainsOnlyWhitespace(slot: HTMLSlotElement) {
  const hasNoElements = !slot.assignedNodes().find(i => i.nodeType !== Node.TEXT_NODE);
  const hasOnlyEmptyTextNodes = !slot
    .assignedNodes()
    .find(i => i.nodeType === Node.TEXT_NODE && i.textContent.trim() !== '');
  return hasNoElements && hasOnlyEmptyTextNodes;
}

export function applySlotContentStates(slot: HTMLSlotElement, element: HTMLElement & { _internals: ElementInternals }) {
  const nodes = slot.assignedNodes();
  const name = slot.name ?? 'slot';

  if (nodes.length) {
    element._internals.states.add(`has-${name}`);
  } else {
    element._internals.states.delete(`has-${name}`);
  }
}

export function getDisplayValue(option: { label?: string; value?: string }): string {
  if (option) {
    return option.label ? option.label : option.value;
  } else {
    return '';
  }
}

/** returns a combination of nve and mlv for a prefixed tag selector */
export function tagSelector(selector: string) {
  return `${selector.replaceAll('nve-', 'nve-')}, ${selector.replaceAll('nve-', 'nve-')}`;
}

/** determines if HTML element matches the given component tag */
export function matchesElementName(element: Partial<HTMLElement>, component: { metadata: { tag: string } }) {
  return (
    element.localName === component.metadata.tag.replace('nve-', 'nve-') ||
    element.localName === component.metadata.tag.replace('nve-', 'nve-')
  );
}

export function createGhostElement(sourceElement: HTMLElement) {
  const ghost = globalThis.document.createElement('div');
  const { width } = sourceElement.getBoundingClientRect();
  ghost.setAttribute('nve-ghost', '');
  ghost.style.minWidth = `${width}px`;
  ghost.style.maxWidth = `${width}px`;
  ghost.style.height = '1px';
  ghost.slot = sourceElement.slot;
  return ghost;
}

export function sameRenderRoot(element1: HTMLElement, element2: HTMLElement) {
  const root1 = element1.getRootNode();
  const root2 = element2.getRootNode();
  const sameShadowRoot = root1 instanceof ShadowRoot && root2 instanceof ShadowRoot && root1 === root2;
  const sameDocumentRoot = root1 === globalThis.document && root2 === globalThis.document;
  return sameShadowRoot || sameDocumentRoot;
}

export function getAnchorNames(element: HTMLElement): string[] {
  return (
    element.style.anchorName
      ?.split(',')
      ?.map(name => name.trim())
      ?.filter(name => name !== 'none' && name !== '') ?? []
  );
}

export function removeAnchorName(element: HTMLElement, name: string) {
  const anchorNames = getAnchorNames(element);
  element.style.anchorName = anchorNames.filter(i => i !== name).join(',');
}

export function appendAnchorName(element: HTMLElement, name: string) {
  element.style.anchorName = Array.from(new Set([...getAnchorNames(element), name])).join(',');
}

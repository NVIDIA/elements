/**
 * Preserves visual DOM ordering when using slots within Shadow DOM
 * See more information/resources on Shadow DOM linear traversal
 * https://nolanlawson.com/2021/02/13/managing-focus-in-the-shadow-dom/
 * https://www.abeautifulsite.net/posts/querying-through-shadow-roots/
 */
export function getFlatDOMTree(node: Node, depth = 10): HTMLElement[] {
  return (
    Array.from(getChildren(node)).reduce((prev: Node[], next: Node) => {
      const nextChild = Array.from(getChildren(next)).map(i => [i, getFlatDOMTree(i, depth)]);
      return [...prev, [next, [...nextChild]]];
    }, []) as HTMLElement[]
  ).flat(depth);
}

export function getChildren(node: Document | Node | HTMLElement): Element[] {
  if (node instanceof Document && node.documentElement) {
    return Array.from(node.documentElement.children); // root document children
  } else if (node instanceof HTMLElement && node.shadowRoot) {
    return Array.from(node.shadowRoot.children); // shadow root direct children
  } else if (node instanceof HTMLSlotElement && node.assignedElements) {
    const slotted = node.assignedElements(); // slotted elements
    return slotted.length ? slotted : Array.from(node.children); // slot fallback
  } else {
    return Array.from((node as HTMLElement).children); // light DOM direct children
  }
}

export function generateId() {
  const uint32 = self.crypto.getRandomValues(new Uint32Array(1))[0];
  return `_${uint32.toString(16)}`;
}

export function getAttributeChanges(element: HTMLElement, attr: string, fn: (attrValue: string) => void) {
  fn(element.getAttribute(attr));
  const observer = new MutationObserver(mutations => {
    mutations.forEach(mutation => {
      if (mutation.attributeName === attr) {
        fn((mutation.target as HTMLElement).getAttribute(attr));
      }
    });
  });
  observer.observe(element, { attributes: true, subtree: true });
  return observer;
}

export function getAttributeListChanges(element: HTMLElement, attrs: string[], fn: (mutation: MutationRecord) => void) {
  const observer = new MutationObserver(mutations => {
    mutations.forEach(mutation => {
      if (attrs.includes(mutation.attributeName)) {
        fn(mutation);
      }
    });
  });
  observer.observe(element, { attributes: true, subtree: true });
  return observer;
}

/**
 * Appends styles to the render root of a given element. This is useful for
 * styles that apply outside the scope of the element's shadow
 * root such as UA styles --webkit-* that won't work in a ::slotted selector
 */
export function appendRootNodeStyle(host: HTMLElement, styles: string) {
  const stylesheet = new CSSStyleSheet();
  stylesheet.replaceSync(styles);
  const root = host.getRootNode() as Document | ShadowRoot;
  if (root.adoptedStyleSheets) {
    const hasStyleSheet = root.adoptedStyleSheets
      .map(s => styleSheetToString(s))
      .find(s => s === styleSheetToString(stylesheet));
    if (!hasStyleSheet) {
      root.adoptedStyleSheets = [...Array.from(root.adoptedStyleSheets), stylesheet];
    }
  }
  return stylesheet;
}

export function styleSheetToString(stylesheet: CSSStyleSheet) {
  return stylesheet.cssRules
    ? Array.from(stylesheet.cssRules)
        .map(rule => rule.cssText || '')
        .join('\n')
    : '';
}

/* used for cases of needing to know a property update outside of lit, example a native input value prop change */
export function getElementUpdate(element: HTMLElement, key: string, callback: (value) => void) {
  if (element.hasAttribute(key)) {
    callback(element.getAttribute(key));
  } else if (element[key] !== undefined) {
    callback(element[key]);
  }

  const updatedProp = Object.getOwnPropertyDescriptor(Object.getPrototypeOf(element), key);
  if (updatedProp) {
    try {
      Object.defineProperty(element, key, {
        get: updatedProp.get,
        set: val => {
          updatedProp.set.call(element, val);
          callback(val);
        }
      });
    } catch {
      // try/catch for cases where another call already defined the prop
    }
  }

  return getAttributeChanges(element, key, val => callback(val));
}

export function clickOutsideElementBounds(event: PointerEvent | MouseEvent, element: HTMLElement) {
  const { left, right, top, bottom } = element.getBoundingClientRect();
  return event.clientX < left || event.clientX > right || event.clientY < top || event.clientY > bottom;
}

/** parses number out of basic calc generated by the design tokens */
export function parseTokenNumber(value: string) {
  return parseInt(value.includes('calc') ? value.split('*')[1].replace('px)', '').trim() : value, 10) || 0;
}

export function define(Element: CustomElementConstructor & { metadata: { version: string; tag: string } }) {
  defineInternal(Element, Element.metadata.tag.replace('nve', 'mlv'));
  defineInternal(class extends Element {}, Element.metadata.tag.replace('mlv', 'nve'));
}

function defineInternal(element: CustomElementConstructor & { metadata: { version: string } }, tagName: string) {
  const { version } = element.metadata;
  const tag = tagName.replace('nve-', '').replace('nve-', '');
  if (!customElements.get(tagName)) {
    customElements.define(tagName, element);
    GlobalStateService.dispatch('NVE_ELEMENT_DEFINE', { elementRegistry: { [tag]: version } });
  } else if (
    GlobalStateService.state.elementRegistry[tag] !== version &&
    globalThis?.location?.hostname === 'localhost'
  ) {
    void import('./audit-logs.js').then(m => console.warn(m.getDuplicatePackageVersionWarning(tag, version)));
  }
}

export function isContextMenuClick(event: MouseEvent) {
  return (event.buttons === 2 && !event.ctrlKey) || (event.buttons === 1 && event.ctrlKey);
}

export function getFlattenedFocusableItems(element: Node, depth = 10) {
  return getFlattenedDOMTree(element, depth).filter((e: HTMLElement) => isFocusable(e)) as HTMLElement[];
}

export function getFlattenedDOMTree(node: Node, depth = 10): HTMLElement[] {
  return (
    Array.from(getChildren(node)).reduce(
      (p: Node[], n: Node) => [...p, [n, [...Array.from(getChildren(n)).map(i => [i, getFlattenedDOMTree(i, depth)])]]],
      []
    ) as HTMLElement[]
  ).flat(depth);
}

export function validKeyNavigationCode(e: KeyboardEvent) {
  return (
    e.code === KeynavCode.End ||
    e.code === KeynavCode.Home ||
    e.code === KeynavCode.PageUp ||
    e.code === KeynavCode.PageDown ||
    e.code === KeynavCode.ArrowUp ||
    e.code === KeynavCode.ArrowDown ||
    e.code === KeynavCode.ArrowLeft ||
    e.code === KeynavCode.ArrowRight
  );
}


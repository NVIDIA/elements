/* eslint-env node */
/* global process */

import htmlMinify from 'html-minifier';
import { transform } from 'lightningcss';

export async function htmlMinifyTransform(content) {
  return process.env.ELEVENTY_RUN_MODE !== 'watch' ? minifyStyleTags(minifyHTML(content)) : content;
}

function minifyStyleTags(html) {
  return html.replace(/<style>([\s\S]*?)<\/style>/g, (match, cssContent) => {
    return `<style>${minifyCSS(cssContent)}</style>`;
  });
}

function minifyCSS(content) {
  let { code } = transform({
    code: Buffer.from(content), // eslint-disable-line no-undef
    minify: true,
    sourceMap: false
  });
  return code.toString();
}

function minifyHTML(html) {
  // Store template contents with unique markers
  const templateContents = [];
  let counter = 0;

  // Replace template contents with markers
  const htmlWithMarkers = html.replace(/<template>([\s\S]*?)<\/template>/g, (match, content) => {
    const marker = `__TEMPLATE_${counter}__`;
    templateContents[counter] = content;
    counter++;
    return `<template>${marker}</template>`;
  });

  // Minify the HTML
  const minifiedHtml = htmlMinify.minify(htmlWithMarkers, {
    includeAutoGeneratedTags: true,
    removeAttributeQuotes: true,
    removeComments: true,
    removeRedundantAttributes: true,
    removeScriptTypeAttributes: true,
    removeStyleLinkTypeAttributes: true,
    sortClassName: true,
    useShortDoctype: true,
    collapseWhitespace: true
  });

  // Restore template contents
  return minifiedHtml.replace(/__TEMPLATE_(\d+)__/g, (match, index) => {
    return templateContents[parseInt(index)];
  });
}
